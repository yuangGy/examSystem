package com.gy.model;

public class SysUserLog {
    private String sysUserLogId;

    private String sysUserName;

    private String sysUserLogDate;

    private String sysUserLogAddress;

    public String getSysUserLogId() {
        return sysUserLogId;
    }

    public void setSysUserLogId(String sysUserLogId) {
        this.sysUserLogId = sysUserLogId;
    }

    public String getSysUserName() {
        return sysUserName;
    }

    public void setSysUserName(String sysUserName) {
        this.sysUserName = sysUserName;
    }

    public String getSysUserLogDate() {
        return sysUserLogDate;
    }

    public void setSysUserLogDate(String sysUserLogDate) {
        this.sysUserLogDate = sysUserLogDate;
    }

    public String getSysUserLogAddress() {
        return sysUserLogAddress;
    }

    public void setSysUserLogAddress(String sysUserLogAddress) {
        this.sysUserLogAddress = sysUserLogAddress;
    }
}